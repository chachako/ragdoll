import Foundation

extension Int {
  /// Converts an `Int` to `Int64`.
  func toInt64() -> Int64 { Int64(self) }
  
  /// Converts an `Int` to `UInt64`.
  func toUInt64() -> UInt64 { UInt64(self) }
  
  /// Converts an `Int` to `UInt`.
  func toUInt() -> UInt { UInt(self) }
}
